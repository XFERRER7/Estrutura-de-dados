package pliha_encadeada;

public class Livro {

	
	private String titulo;
	private String autor;
	private String editora;
	
	
	
	public Livro(String t, String a, String e) {
		this.titulo = t;
		this.autor = a;
		this.editora = e;
	}
	
	
	public String getLivro() {
	
		return (titulo + "\n" + autor + "\n" + editora + "\n");
		
		
		
		
		
		
		
		
		
		
	}
	
	
	
	
	
	
	
	
	
	
	
}
