package estudoPilhaArranjos;

public class Aluno {

	
	private int turma;
	private String nome;
	private int idade;
	
	
	
	
	
	
	
	
	public int getTurma() {
		return turma;
	}
	public void setTurma(int turma) {
		this.turma = turma;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	
	
	public Aluno(String n, int i, int t) {
		
		this.nome = n;
		this.idade = i;
		this.turma = t;
		
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
